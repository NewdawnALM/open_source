#ifndef __AE_H__
#define __AE_H__

#include <time.h>
#include <queue>    //使用std的优先队列实现最小堆
#include <set>

#define AE_OK 0
#define AE_ERR -1

#define AE_NONE 0       /* No events registered. */
#define AE_READABLE 1   /* Fire when descriptor is readable. */
#define AE_WRITABLE 2   /* Fire when descriptor is writable. */
#define AE_BARRIER 4    /* With WRITABLE, never fire the event if the
                           READABLE event already fired in the same event
                           loop iteration. Useful when you want to persist
                           things to disk before sending replies, and want
                           to do that in a group fashion. */
// AE_BARRIER 写先处理标记。设置了这个后当fd既可读也可写时，先调用写处理函数，再调用读处理函数。（当读写同时触发时默认是先处理读的）

#define AE_FILE_EVENTS 1
#define AE_TIME_EVENTS 2
#define AE_ALL_EVENTS (AE_FILE_EVENTS|AE_TIME_EVENTS)
#define AE_DONT_WAIT 4
#define AE_CALL_AFTER_SLEEP 8

#define AE_NOMORE -1
#define AE_DELETED_EVENT_ID -1

/* Macros */
#define AE_NOTUSED(V) ((void) V)

struct aeEventLoop;

/* Types and data structures */
typedef void aeFileProc(struct aeEventLoop *eventLoop, int fd, void *clientData, int mask);
typedef int aeTimeProc(struct aeEventLoop *eventLoop, long long id, void *clientData);  //超时事件回调函数，返回AE_NOMORE会移除该定时器，返回其它值则以当前时间加上返回值设置新的定时器
typedef void aeEventFinalizerProc(struct aeEventLoop *eventLoop, void *clientData);  //定时器删除回调函数(只有在processTimeEvents(aeMain)中遍历定时器时才会触发，aeDeleteTimeEvent不会触发)
typedef void aeBeforeSleepProc(struct aeEventLoop *eventLoop);

/* File event structure */
typedef struct aeFileEvent {
    int mask; /* one of AE_(READABLE|WRITABLE|BARRIER) */
    aeFileProc *rfileProc;
    aeFileProc *wfileProc;
    void *clientData;
} aeFileEvent;

/* Time event structure */
typedef struct aeTimeEvent {
    long long id; /* time event identifier. */
    long when_sec; /* seconds */
    long when_ms; /* milliseconds */
    aeTimeProc *timeProc;
    aeEventFinalizerProc *finalizerProc;
    void *clientData;
    // struct aeTimeEvent *prev;
    // struct aeTimeEvent *next;
    bool operator < (const aeTimeEvent &rhs) const {
        return when_sec == rhs.when_sec ? when_ms > rhs.when_ms : when_sec > rhs.when_sec;
    }
} aeTimeEvent;

/* A fired event */
typedef struct aeFiredEvent {
    int fd;
    int mask;
} aeFiredEvent;

/* State of an event based program */
typedef struct aeEventLoop {
    int maxfd;   /* highest file descriptor currently registered */
    int setsize; /* max number of file descriptors tracked */
    aeFileEvent *events; /* Registered events */
    aeFiredEvent *fired; /* Fired events */
    time_t lastTime;     /* Used to detect system clock skew */
    long long timeEventNextId;
    // aeTimeEvent *timeEventHead;
    std::priority_queue<aeTimeEvent> *timeMinHead;
    std::set<long long> *setDelTimeId;
    int stop;
    void *apidata; /* This is used for polling API specific data */
    aeBeforeSleepProc *beforesleep;
    aeBeforeSleepProc *aftersleep;
} aeEventLoop;

/* Prototypes */
aeEventLoop *aeCreateEventLoop(int setsize);
void aeDeleteEventLoop(aeEventLoop *eventLoop);
void aeStop(aeEventLoop *eventLoop);
int aeCreateFileEvent(aeEventLoop *eventLoop, int fd, int mask,
        aeFileProc *proc, void *clientData);
void aeDeleteFileEvent(aeEventLoop *eventLoop, int fd, int mask);
int aeGetFileEvents(aeEventLoop *eventLoop, int fd);
long long aeCreateTimeEvent(aeEventLoop *eventLoop, long long milliseconds,    //距离当前时间milliseconds毫秒后触发
        aeTimeProc *proc, void *clientData,     //超时时间回调函数和对应的参数
        aeEventFinalizerProc *finalizerProc);   //定时器删除回调函数
int aeDeleteTimeEvent(aeEventLoop *eventLoop, long long id);    //删除定时器(只设置删除标记，在遍历定时事件时才作真正的删除操作)
int aeProcessEvents(aeEventLoop *eventLoop, int flags);
int aeWait(int fd, int mask, long long milliseconds);
void aeMain(aeEventLoop *eventLoop);
const char *aeGetApiName(void);
void aeSetBeforeSleepProc(aeEventLoop *eventLoop, aeBeforeSleepProc *beforesleep);
void aeSetAfterSleepProc(aeEventLoop *eventLoop, aeBeforeSleepProc *aftersleep);
int aeGetSetSize(aeEventLoop *eventLoop);
int aeResizeSetSize(aeEventLoop *eventLoop, int setsize);

#endif
